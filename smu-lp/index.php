      checkboxError.style.display = 'none';
      checkboxError.style.marginTop = '5px';
      checkbox.parentNode.insertBefore(checkboxError, checkbox.nextSibling);
    }

    checkbox.addEventListener('change', function () {
      if (checkbox.checked) {
